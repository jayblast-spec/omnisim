export function selectAgentsForSimulation(type: string, count: number): AgentProfile[] {
  const tagged = agentProfiles.filter((a) => a.tags.includes(type));
  const others = agentProfiles.filter((a) => !a.tags.includes(type));

  const shuffled = [...tagged].sort(() => Math.random() - 0.5);
  const shuffledOthers = [...others].sort(() => Math.random() - 0.5);

  const combined = [...shuffled, ...shuffledOthers];
  return combined.slice(0, count);
}
